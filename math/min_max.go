package math

// MaxInt 返回多个 int 中最大的那个
func MaxInt(nums ...int) int {
	m := nums[0]
	for i := 1; i < len(nums); i++ {
		if nums[i] > m {
			m = nums[i]
		}
	}
	return m
}

